package com.kang.backup.model;

public class MemoModel {

    private String memoTitle;

    private String memoContext;

    private String memoKey;

    private String memoCnt;

    public String getMemoTitle() {
        return memoTitle;
    }

    public void setMemoTitle(String memoTitle) {
        this.memoTitle = memoTitle;
    }

    public String getMemoContext() {
        return memoContext;
    }

    public void setMemoContext(String memoContext) {
        this.memoContext = memoContext;
    }

    public String getMemoKey() {
        return memoKey;
    }

    public void setMemoKey(String memoKey) {
        this.memoKey = memoKey;
    }

    public String getMemoCnt() {
        return memoCnt;
    }

    public void setMemoCnt(String memoCnt) {
        this.memoCnt = memoCnt;
    }
}
